public class UpperCase {
    public static void main(String[] args){
        String a1="I love Eugeniy";
        String result=a1.toUpperCase();
        System.out.println("Result: "+result);

        a1="0 and 1 and 2 and 3 and 4";
        result=a1.toUpperCase();
        System.out.println("Result: "+result);

        a1="LES MISERABLES";
        result=a1.toUpperCase();
        System.out.println("Result: " +result);
    }
}
