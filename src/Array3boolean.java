public class Array3boolean {
    public static void main(String[] args){
        boolean[] array3= new boolean[1000];
        for(int i=0;i<array3.length;i++){
            System.out.println(array3[i]);
        }
    }
}

/*
boolean о умолчанию всегда false, итого мы получаем 1000 false
Также обращаю внимание, что данный цикл не имеет логики
 */