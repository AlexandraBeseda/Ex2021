public class Flower {
    String name;
    String colour;
    int age;

    public Flower(){}

    public Flower(String myName, String myColour){
        name=myName;
        colour=myColour;
    }
    public Flower(String myName, String myColour, int myAge){
        name=myName;
        colour=myColour;
        age=myAge;
    }
}
